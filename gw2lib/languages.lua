		elseif ( arg == "3" ) then gCurrentLanguage = "ru" 
		elseif ( arg == "4" ) then gCurrentLanguage = "fr" 
		elseif ( arg == "5" ) then gCurrentLanguage = "tr" 
		end
	end	
end

